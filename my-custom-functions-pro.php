 */
defined('ABSPATH') or die("Restricted access!");

/**
 * Register text domain
 *
 * @since 0.1
 */
function anarcho_cfunctions_pro_textdomain() {
	load_plugin_textdomain( 'anarcho_cfunctions_pro', false, dirname( plugin_basename( __FILE__ ) ) . '/languages/' );
}
add_action( 'init', 'anarcho_cfunctions_pro_textdomain' );

/**
 * Print direct link to Custom Functions admin page
 *
 * Fetches array of links generated by WP Plugin admin page ( Deactivate | Edit )
 * and inserts a link to the Custom Functions admin page
 *
 * @since  0.1
 * @param  array $links Array of links generated by WP in Plugin Admin page.
 * @return array        Array of links to be output on Plugin Admin page.
 */
function anarcho_cfunctions_pro_settings_link( $links ) {
	$settings_page = '<a href="' . admin_url( 'themes.php?page=my-custom-functions-pro.php' ) .'">' . __( 'Settings', 'anarcho_cfunctions_pro' ) . '</a>';
	array_unshift( $links, $settings_page );
	return $links;
}
$plugin = plugin_basename( __FILE__ );
add_filter( "plugin_action_links_$plugin", 'anarcho_cfunctions_pro_settings_link' );

/**
 * Register "Custom Functions" submenu in "Appearance" Admin Menu
 *
 * @since 0.1
 */
function anarcho_cfunctions_pro_register_submenu_page() {
	add_theme_page( __( 'My Custom Functions Pro', 'anarcho_cfunctions_pro' ), __( 'Custom Functions', 'anarcho_cfunctions_pro' ), 'edit_theme_options', basename( __FILE__ ), 'anarcho_cfunctions_pro_render_submenu_page' );
}
add_action( 'admin_menu', 'anarcho_cfunctions_pro_register_submenu_page' );

/**
 * Attach Settings Page
 *
 * @since 0.1
 */
require_once( plugin_dir_path( __FILE__ ) . 'inc/settings_page.php' );

/**
 *  Enqueue jQuery library, Repeater script, CodeMirror scripts and styles
 *
 * @since 0.1
 */
function anarcho_cfunctions_pro_load_scripts($hook) {
    if ( 'appearance_page_my-custom-functions-pro' != $hook ) {
        return;
    }

    // JQuery library
    wp_enqueue_script('jquery');

    // Repeater
    wp_enqueue_script('anarcho-repeater-field', plugin_dir_url(__FILE__) . 'inc/repeater.js');

    // CodeMirror
    wp_enqueue_script('codemirror', plugin_dir_url(__FILE__) . 'inc/codemirror/codemirror-compressed.js');
    wp_enqueue_style('codemirror_style', plugin_dir_url(__FILE__) . 'inc/codemirror/codemirror.css');

    // Styles
    wp_enqueue_style('styles', plugin_dir_url(__FILE__) . 'inc/styles.css');
}
add_action('admin_enqueue_scripts', 'anarcho_cfunctions_pro_load_scripts');

/**
 * Register settings
 *
 * @since 0.1
 */
function anarcho_cfunctions_pro_register_settings() {
	register_setting( 'anarcho_cfunctions_pro_settings_group', 'anarcho_cfunctions_pro_labels' );
	register_setting( 'anarcho_cfunctions_pro_settings_group', 'anarcho_cfunctions_pro_functions' );
	register_setting( 'anarcho_cfunctions_pro_settings_group', 'anarcho_cfunctions_pro_error' );
}
add_action( 'admin_init', 'anarcho_cfunctions_pro_register_settings' );

/**
 * Execute My Custom Functions Pro
 *
 * @since 0.1
 */
function anarcho_cfunctions_pro_exec() {
     // Read from BD
     $functions = get_option( 'anarcho_cfunctions_pro_functions' );
     //$functions = $options[@];

     // Cleaning
     $functions = trim( $functions );
     $functions = trim( $functions, '<?php' );

     // Parsing and execute safe
     if ( !empty($functions) ) {
        if( false === @eval( $functions ) ) {
            //ERROR
            update_option( 'anarcho_cfunctions_pro_error', '1' );
        } else {
            // Reset error value
            update_option( 'anarcho_cfunctions_pro_error', '0' );
        }
     }
}
anarcho_cfunctions_pro_exec();


/**
 * Delete Options on Uninstall
 *
 * @since 0.1
 */
function anarcho_cfunctions_pro_uninstall() {
	delete_option( 'anarcho_cfunctions_pro_labels' );
	delete_option( 'anarcho_cfunctions_pro_functions' );
	delete_option( 'anarcho_cfunctions_pro_error' );
}
register_uninstall_hook( __FILE__, 'anarcho_cfunctions_pro_uninstall' );


